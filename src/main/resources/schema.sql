DROP TABLE IF EXISTS TB_USER;

CREATE TABLE TB_USER
(
    USER_ID         INT AUTO_INCREMENT NOT NULL,
    USER_LOGIN_ID   VARCHAR(20) NOT NULL,
    USER_ROLE       VARCHAR(20) DEFAULT 'USER' NOT NULL,
    USER_NAME       VARCHAR(20) NOT NULL,
    USER_PASSWD     VARCHAR(400) NOT NULL,
    USER_EMAIL      VARCHAR(100) NOT NULL,
    IS_USED         CHAR(1) DEFAULT 'Y' NOT NULL,
    IS_DEL          CHAR(1) DEFAULT 'N' NOT NULL,
    SYS_DATE       DATETIME DEFAULT NOW() NOT NULL,
    REG_DATE       DATETIME ,
    CONSTRAINT USER_PK PRIMARY KEY (USER_ID)
);

